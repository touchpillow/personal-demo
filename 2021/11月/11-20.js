var findLeastNumOfUniqueInts = function (arr, k) {
  const map = new Map();
  for (const n of arr) {
    map.set(n, (map.get(n) || 0) + 1);
  }
  const list = [...map.values()].sort((a, b) => a - b);
  const l = list.length;
  let sum = 0;
  for (let i = 0; i < l; i++) {
    sum += list[i];
    if (sum > k) return l - i;
  }
  return 0;
};
